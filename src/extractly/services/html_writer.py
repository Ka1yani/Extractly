"""Task 3B: HTML Mirror Form Generator.

Generates standalone HTML files that replicate the document's structure
using <fieldset> grouping with pre-filled <input> fields.

Usage:
    writer = HtmlWriter()
    writer.write_all(documents, template, Path("output/html"))
"""

import html
import logging
from pathlib import Path

from extractly.models.template import MasterTemplate
from extractly.models.extraction import ExtractedDocument

logger = logging.getLogger(__name__)

# Clean, readable inline CSS for the HTML forms
